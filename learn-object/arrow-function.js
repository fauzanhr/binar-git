let hello = function() {
    return 'Hello World'
}

// Arrow Function
let hello1 = () => {
    return 'Hello World'
}

// Function with 2 param use template literal string
let printName = (firstName, lastName) => {
    return `Hello my Name is ${firstName} ${lastName}`
}

let printName2 = (firstName, lastName) => `Hello my Name is ${firstName} ${lastName}`


// Shorthand Arrow Function
let hello2 = () => 'Hello World'

console.log(hello())
console.log(hello1())
console.log(hello2())
console.log(printName('Anggara', 'Setiawan'))

